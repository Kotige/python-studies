# Operadores in e not in
# Strings são iteráveis
# 0 1 2 3 4 5
# V I T O R
nome = 'Vitor'
print(nome[2])
# ou pelo índice negativo
print(nome[-3])
#
#
# Operador IN
print ('ito' in nome) # retorna true
print ('abba' in nome) # retorna false
#
#
# Operador NOT IN
print ('abba' not in nome) # retorna true
print ('ito' not in nome) # retorna false
