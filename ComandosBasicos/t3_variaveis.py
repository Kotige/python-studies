# Variáveis são usadas para salvar algum tipo de dado na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar números e underline _ (snake case).
# O sinal de = é operador de atribuição. Ele é usado para atribuir um valor
# a um nome (variável). 
# Exemplo: nome_variavel = expressão
nome_completo = 'Vitor Barra'
soma = 2+2
print(nome_completo, soma)